package Queue;
/////////////////////////////////////////////////////////////////////
public abstract class AbstractQueue<E>
{
	protected int size;
	//-------------------------------------------------------------------
	public AbstractQueue()
	{
		this.size = 0;
	}
	//-------------------------------------------------------------------
	public boolean isEmpty()
	{
		return (getSize() == 0);
	}
	//-------------------------------------------------------------------
	public int getSize()
	{
		return size;
	}
	//-------------------------------------------------------------------
	public abstract E dequeue();
	//-------------------------------------------------------------------
	public abstract void enqueue(E element);
	//-------------------------------------------------------------------
	public abstract E peek();
	//-------------------------------------------------------------------
}
/////////////////////////////////////////////////////////////////////